def s(run, sz=10.5, b=False, c=None):
    run.font.name='宋体'; run.font.size=Pt(sz)
    rp=run._element.get_or_add_rPr(); rf=OxmlElement('w:rFonts'); rf.set(qn('w:eastAsia'),'宋体'); rp.insert(0,rf)
    if b: run.bold=True
    if c: run.font.color.rgb=RGBColor(*c)

def H(t, lv=1):
    h=doc.add_heading(t,level=lv)
    for r in h.runs: s(r,14 if lv==1 else 12 if lv==2 else 11)

def P(t, sz=10.5, b=False, c=None, a=None):
    p=doc.add_paragraph(); p.paragraph_format.line_spacing=1.0
    r=p.add_run(t); s(r,sz,b,c)
    if a is not None: p.alignment=a
    return p

def B(t, sz=10.5):
    p=doc.add_paragraph(); p.paragraph_format.line_spacing=1.0; p.paragraph_format.left_indent=Cm(0.6)
    r=p.add_run(t); s(r,sz)

def T(hd, rs):
    t=doc.add_table(rows=1+len(rs),cols=len(hd)); t.style='Table Grid'
    for i,h in enumerate(hd):
        c=t.rows[0].cells[i]; c.text=h
        for p in c.paragraphs:
            p.paragraph_format.line_spacing=1.0
            for r in p.runs: r.font.name='宋体'; r.font.size=Pt(9)
    for ri,row in enumerate(rs):
        for ci,val in enumerate(row):
            c=t.rows[ri+1].cells[ci]; c.text=str(val)
            for p in c.paragraphs:
                p.paragraph_format.line_spacing=1.0
                for r in p.runs: r.font.name='宋体'; r.font.size=Pt(9)
    doc.add_paragraph()

def section(s):
    doc.add_section()
    return P(s, sz=11, b=True)

